PASS = "PASS"
FAIL = "FAIL"
_results: list[tuple[str, str]] = []


def check(name: str, condition: bool, detail: str = "") -> bool:
    status = PASS if condition else FAIL
    msg = f"  [{status}] {name}"
    if detail:
        msg += f" -- {detail}"
    print(msg)
    _results.append((name, status))
    return condition


def section(title: str) -> None:
    print(f"\n{'='*60}")
    print(f"  {title}")
    print("=" * 60)


# ---------------------------------------------------------------------------
# Known sentences placed deliberately in the sample files.
#
# With the paragraph-aware ~1500-char chunker, sample.txt's first chunk
# covers the opening paragraphs including the CAP theorem paragraph, so
# the known sentence lands in chunk 0.
#
# sample.md: K-means paragraph follows multiple supervised-learning paragraphs
# that together exceed 1500 chars, so K-means lands in chunk 1.
# ---------------------------------------------------------------------------
KNOWN_TXT_SENTENCE = (
    "The CAP theorem, formulated by Eric Brewer in 2000, states that a "
    "distributed system cannot simultaneously provide all three"
)
KNOWN_TXT_CHUNK = 0  # expected 0-based chunk index (paragraph-aware chunker)

KNOWN_MD_SENTENCE = (
    "K-means clustering partitions data into k clusters by iteratively"
)
KNOWN_MD_CHUNK = 1  # expected 0-based chunk index (paragraph-aware chunker)

# Paragraph-aware chunker target (chars).  Single source of truth lives in
# ingest_lib; the test references it so changing the knob never breaks this test.
# Chunks may vary somewhat with paragraph boundaries; we check the target is
# roughly met.
TARGET_CHARS = ingest_lib.DEFAULT_TARGET_CHARS
# Paragraph-aware chunks may be smaller when approaching document end;
# only check that chunks at least 2 before the last are >= 50% of target.
# This avoids false failures on short trailing paragraphs.
TOLERANCE = 0.50   # chunks (excluding last 2) should be >= 50% of target_chars

# Minimum overlap in chars between consecutive chunks (1-para overlap).
# One paragraph in sample.txt is typically 300-800 chars.
MIN_OVERLAP_CHARS = 50


# ---------------------------------------------------------------------------
# PDF generation (via reportlab) -- create a tiny valid PDF in a temp file
# ---------------------------------------------------------------------------

def make_test_pdf(dest: Path) -> str:
    """
    Write a minimal PDF with a known sentence and return that sentence.
    Uses reportlab (declared in this script's inline deps).
    """
    from reportlab.lib.pagesizes import LETTER  # type: ignore[import-untyped]
    from reportlab.pdfgen import canvas  # type: ignore[import-untyped]

    known = "The PDF extraction sentinel: distributed systems use replication."

    c = canvas.Canvas(str(dest), pagesize=LETTER)
    width, height = LETTER
    text_obj = c.beginText(72, height - 72)
    text_obj.setFont("Helvetica", 12)
    text_obj.textLine("Test PDF document for slm-rag P2.")
    text_obj.textLine("")
    text_obj.textLine(known)
    text_obj.textLine("")
    text_obj.textLine(
        "This document is auto-generated by tests/p2_chunk.py to verify "
        "that extract_text correctly reads PDF files via pypdf."
    )
    c.drawText(text_obj)
    c.save()
    return known


