def main() -> int:
    samples_dir = PROJECT_ROOT / "samples"
    tmp_dir = PROJECT_ROOT / "samples"  # reuse samples dir for temp PDF

    print("=" * 60)
    print("  slm-rag P2 -- text extraction + chunking tests")
    print("  (paragraph-aware chunker, ~1500 chars + 1-para overlap)")
    print("=" * 60)

    test_txt(samples_dir)
    test_md(samples_dir)
    test_pdf(tmp_dir)
    test_unsupported_extension()
    test_chunk_text_unit()

    # Summary
    print(f"\n{'='*60}")
    passed = sum(1 for _, s in _results if s == PASS)
    failed = sum(1 for _, s in _results if s == FAIL)
    print(f"  Results: {passed} passed, {failed} failed  ({len(_results)} checks)")
    print("=" * 60)

    if failed > 0:
        print("\nFAILED checks:")
        for name, status in _results:
            if status == FAIL:
                print(f"  - {name}")
        return 1

    print("\nAll checks PASSED.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
